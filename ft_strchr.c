#include "libft.h"

char	*ft_strchr(const char *str, int ch)
{
	char	*s;
	char	*c;

	s = str;
	c = ch;
	while (*s)
	{
		if (*s == c)
			return (s);
		s++;
	}
	if (c == '\0')
		return (s);
	return (NULL);
}

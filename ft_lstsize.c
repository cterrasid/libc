/* -------------------------------------------------------------------------- */
/* Counts the number of elements in a list.								      */
/* -------------------------------------------------------------------------- */

#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	len;

	len = 0;
	while (lst)
	{
		lst = lst->next;
		len++;
	}
	return (len);
}

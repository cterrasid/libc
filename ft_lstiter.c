/* -------------------------------------------------------------------------- */
/* Loops the 'lst' list and apply the function 'f' to the content of each	  */
/* element.																	  */
/* -------------------------------------------------------------------------- */

#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst)
	{
		f(lst->content);
		lst = lst->next;
	}
}
